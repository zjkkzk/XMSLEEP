    fun getAllNovels(): List<NovelItem> = essays + chengyuStories + fables + stories

    fun getNovelsByCategory(category: NovelCategory): List<NovelItem> {
        return when (category) {
            NovelCategory.ESSAY -> essays
            NovelCategory.CHENGYU -> chengyuStories
            NovelCategory.FABLE -> fables
            NovelCategory.STORY -> stories
        }
    }

    fun getNovelById(id: String): NovelItem? {
        return getAllNovels().find { it.id == id }
    }

    fun getRandomNovel(category: NovelCategory? = null): NovelItem {
        val list = category?.let { getNovelsByCategory(it) } ?: getAllNovels()
        return list.random()
    }
